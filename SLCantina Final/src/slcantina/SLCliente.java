    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String s = jFormattedTextField1.getText().replace(".", "").replace("-", "");
        if(s.equals("")){
            JOptionPane.showInternalMessageDialog(SLPrincipal.jDesktopPane1, "O campo CPF deve estar preenchido");
        }else if(jTextField1.getText().equals("")){
            JOptionPane.showInternalMessageDialog(SLPrincipal.jDesktopPane1, "O campo Nome deve estar preenchido");
        }else if(jComboBox1.getSelectedIndex()==0){
            JOptionPane.showInternalMessageDialog(SLPrincipal.jDesktopPane1, "Selecione um tipo de cliente");
        }else{
            String a = SLCliente.jTextField1.getText();
             int c = SLCliente.jComboBox1.getSelectedIndex();
            SLPrincipal.DB.insert("INSERT INTO cliente values(" + s + ", '" + a + "', '" + c + "')");
            JOptionPane.showInternalMessageDialog(SLPrincipal.jDesktopPane1, "Cliente cadastrado com sucesso!");
            jTextField1.setText("");
            jFormattedTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


