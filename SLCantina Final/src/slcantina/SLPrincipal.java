    public static void spawnNewWindow(JInternalFrame m)
    {
        Vector<Boolean> aux1 = new Vector<>();
        Vector<JInternalFrame> ji = new Vector<>();
        Vector<JDesktopIcon> jd = new Vector<>();
        try {
        for(Component c : Arrays.asList(jDesktopPane1.getComponents()))
        {
            if(c.getClass().equals(m.getClass()))
            {
                aux1.add(false);
                if(c instanceof JInternalFrame) ((JInternalFrame) c).setIcon(true);
                else if(c instanceof JDesktopIcon) jd.add((JDesktopIcon) c);
            } else aux1.add(true);
        }
        for(JInternalFrame f : ji){
            f.setIcon(true);
            System.out.println(f.getClass());}
        if(!aux1.contains(false)){
            m.setLocation(((Toolkit.getDefaultToolkit().getScreenSize().width-2)-m.getSize().width) / 2, ((Toolkit.getDefaultToolkit().getScreenSize().height-2)-m.getSize().height) / 2);
            jDesktopPane1.add(m);
            m.setVisible(true);
        } else 
        {

        }
        } catch (PropertyVetoException ex) {
                Logger.getLogger(SLPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

